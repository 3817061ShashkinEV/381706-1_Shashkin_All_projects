#include "Point.h"
#include <iostream>

TPoint::TPoint(double _x, double _y)
{
  x = _x;
  y = _y;
}
// ---------------------------------------------------------------------------
TPoint::TPoint()
{
  x = 0;
  y = 0;
}
// ---------------------------------------------------------------------------
TPoint::TPoint(TPoint &obj)
{
  x = obj.x;
  y = obj.y;
}
// ---------------------------------------------------------------------------
double TPoint::GetX()
{
  return x;
}
// ---------------------------------------------------------------------------
double TPoint::GetY()
{
  return y;
}
// ---------------------------------------------------------------------------
void TPoint::SetX(double _x)
{
  x = _x;
}
// ---------------------------------------------------------------------------
void TPoint::SetY(double _y)
{
  y = _y;
}
// ---------------------------------------------------------------------------
bool TPoint::operator==(TPoint &obj)
{
  return (x == obj.x) && (y == obj.y);
}
// ---------------------------------------------------------------------------
void TPoint::Show()
{
  std::cout << "(" << x << " , " << y << ")";
}